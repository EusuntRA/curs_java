public class LiteraIf {
	
	public static void main (String[] args) {
		char litera = 'z';
		
        if(litera == 'a' || litera == 'e' || litera == 'i' || litera == 'o' || litera == 'u' )
            System.out.println("litera " + litera + " e o vocala");
        else
            System.out.println("litera " + litera + " e o consoana");
	}
}