package com.step.ex4.comparator;

public class NumbersComparator {


    public int maxim(int numb1,int numb2)
    {
        if(numb1>numb2)
        {return numb1;}
        else {return numb2;}
    }

    public int minim(int numb1,int numb2)
    {
        if(numb1<numb2)
        {return numb1;}
        else {return numb2;}
    }
}


