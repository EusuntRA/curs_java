package com.step.ex3.calculator;

public class Calculator {

    public double suma(double numb1, double numb2) {
        return numb1 + numb2;
    }

    public double scad(double numb1, double numb2) {
        return numb1 - numb2;
    }

    public double multiple(double numb1, double numb2) {
        return numb1 * numb2;
    }

    public double divide(double numb1, double numb2) {
        return numb1 / numb2;
    }
}