package com.step.ex3.calculator;

public class Calculator {
    public double numb1;
    public double numb2;

    public int sum(int numb1, int numb2) {
        return numb1 + numb2;
    }

    public int min(int numb1, int numb2) {
        return numb1 - numb2;
    }

    public int multiple(int numb1, int numb2) {
        return numb1 * numb2;
    }

    public double divide(double numb1, double numb2) {
        return numb1 / numb2;
    }
}