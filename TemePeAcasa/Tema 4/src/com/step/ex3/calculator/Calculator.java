package com.step.ex3.calculator;

public class Calculator {

    public static double suma(double numb1, double numb2) {
        return numb1 + numb2;
    }

    public static double scad(double numb1, double numb2) {
        return numb1 - numb2;
    }

    public static double multiple(double numb1, double numb2) {
        return numb1 * numb2;
    }

    public static double divide(double numb1, double numb2) {
        return numb1 / numb2;
    }
}